-- WhatsNaija AI Database Schema
-- PostgreSQL / Supabase
-- Run this script in your Supabase SQL Editor

-- ============================================================================
-- TABLES
-- ============================================================================

-- Businesses table: Each business owner account
CREATE TABLE IF NOT EXISTS businesses (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    owner_id UUID REFERENCES auth.users(id) ON DELETE CASCADE,
    name TEXT NOT NULL,
    whatsapp_number TEXT UNIQUE NOT NULL,        -- e.g. "+2348012345678"
    phone_number_id TEXT NOT NULL,               -- Meta's Phone Number ID
    waba_id TEXT NOT NULL,                       -- WhatsApp Business Account ID
    paystack_secret TEXT,                        -- Encrypted Paystack secret key
    business_type TEXT,                          -- "fashion", "food", "beauty", etc.
    tone_preference TEXT DEFAULT 'casual',       -- "formal" | "casual" | "pidgin"
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now()
);

-- Knowledge base: Products, FAQs, policies for each business
CREATE TABLE IF NOT EXISTS knowledge_base (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    business_id UUID REFERENCES businesses(id) ON DELETE CASCADE,
    type TEXT NOT NULL,                          -- "product" | "faq" | "policy" | "greeting"
    question TEXT,                               -- For FAQs
    answer TEXT NOT NULL,
    product_name TEXT,                           -- For products
    price NUMERIC(10, 2),                        -- For products (in Naira)
    description TEXT,                            -- For products
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now()
);

-- Conversations: Each WhatsApp conversation thread
CREATE TABLE IF NOT EXISTS conversations (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    business_id UUID REFERENCES businesses(id) ON DELETE CASCADE,
    customer_phone TEXT NOT NULL,
    customer_name TEXT,
    status TEXT DEFAULT 'bot',                   -- "bot" | "human" | "closed"
    started_at TIMESTAMPTZ DEFAULT now(),
    last_message_at TIMESTAMPTZ DEFAULT now(),
    UNIQUE(business_id, customer_phone)          -- One active conversation per customer per business
);

-- Messages: Individual messages in conversations
CREATE TABLE IF NOT EXISTS messages (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    conversation_id UUID REFERENCES conversations(id) ON DELETE CASCADE,
    role TEXT NOT NULL,                          -- "customer" | "bot" | "owner"
    content TEXT NOT NULL,
    wa_message_id TEXT UNIQUE,                   -- WhatsApp message ID (for deduplication)
    wa_status TEXT,                              -- "sent" | "delivered" | "read" | "failed"
    sent_at TIMESTAMPTZ DEFAULT now()
);

-- Leads: Qualified customer records captured during conversations
CREATE TABLE IF NOT EXISTS leads (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    business_id UUID REFERENCES businesses(id) ON DELETE CASCADE,
    conversation_id UUID REFERENCES conversations(id) ON DELETE SET NULL,
    customer_name TEXT,
    customer_phone TEXT NOT NULL,
    product_interest TEXT,
    budget_range TEXT,
    timeline TEXT,
    status TEXT DEFAULT 'new',                   -- "new" | "contacted" | "converted" | "lost"
    notes TEXT,
    captured_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now()
);

-- Payment links: Paystack payment links generated by bot
CREATE TABLE IF NOT EXISTS payment_links (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    business_id UUID REFERENCES businesses(id) ON DELETE CASCADE,
    conversation_id UUID REFERENCES conversations(id) ON DELETE SET NULL,
    paystack_ref TEXT UNIQUE NOT NULL,
    amount NUMERIC(10, 2) NOT NULL,
    description TEXT,
    customer_phone TEXT,
    status TEXT DEFAULT 'sent',                  -- "sent" | "clicked" | "paid" | "failed"
    payment_url TEXT,
    created_at TIMESTAMPTZ DEFAULT now(),
    paid_at TIMESTAMPTZ
);

-- Bookings: Appointment bookings made through bot
CREATE TABLE IF NOT EXISTS bookings (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    business_id UUID REFERENCES businesses(id) ON DELETE CASCADE,
    conversation_id UUID REFERENCES conversations(id) ON DELETE SET NULL,
    customer_name TEXT,
    customer_phone TEXT NOT NULL,
    service TEXT,
    scheduled_at TIMESTAMPTZ NOT NULL,
    duration_minutes INTEGER DEFAULT 60,
    status TEXT DEFAULT 'pending',               -- "pending" | "confirmed" | "cancelled" | "completed"
    notes TEXT,
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now()
);

-- Business hours: Available time slots for bookings
CREATE TABLE IF NOT EXISTS business_hours (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    business_id UUID REFERENCES businesses(id) ON DELETE CASCADE,
    day_of_week INTEGER NOT NULL,               -- 0=Sunday, 1=Monday, ..., 6=Saturday
    start_time TIME NOT NULL,
    end_time TIME NOT NULL,
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMPTZ DEFAULT now()
);

-- Conversation state: Extracted facts and context for ongoing conversations
CREATE TABLE IF NOT EXISTS conversation_state (
    conversation_id UUID PRIMARY KEY REFERENCES conversations(id) ON DELETE CASCADE,
    customer_name TEXT,
    product_interest TEXT,
    budget_range TEXT,
    timeline TEXT,
    last_bot_question TEXT,
    extracted_facts JSONB,                       -- Flexible key-value storage
    updated_at TIMESTAMPTZ DEFAULT now()
);

-- ============================================================================
-- INDEXES FOR PERFORMANCE
-- ============================================================================

CREATE INDEX IF NOT EXISTS idx_businesses_owner ON businesses(owner_id);
CREATE INDEX IF NOT EXISTS idx_businesses_phone_number_id ON businesses(phone_number_id);
CREATE INDEX IF NOT EXISTS idx_businesses_whatsapp_number ON businesses(whatsapp_number);

CREATE INDEX IF NOT EXISTS idx_knowledge_base_business ON knowledge_base(business_id, type, is_active);

CREATE INDEX IF NOT EXISTS idx_conversations_business ON conversations(business_id, status, last_message_at DESC);
CREATE INDEX IF NOT EXISTS idx_conversations_customer ON conversations(business_id, customer_phone);

CREATE INDEX IF NOT EXISTS idx_messages_conversation ON messages(conversation_id, sent_at DESC);
CREATE INDEX IF NOT EXISTS idx_messages_wa_id ON messages(wa_message_id);

CREATE INDEX IF NOT EXISTS idx_leads_business ON leads(business_id, status, captured_at DESC);
CREATE INDEX IF NOT EXISTS idx_leads_phone ON leads(customer_phone);

CREATE INDEX IF NOT EXISTS idx_payment_links_business ON payment_links(business_id, status, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_payment_links_ref ON payment_links(paystack_ref);

CREATE INDEX IF NOT EXISTS idx_bookings_business ON bookings(business_id, status, scheduled_at);
CREATE INDEX IF NOT EXISTS idx_bookings_scheduled ON bookings(scheduled_at);

CREATE INDEX IF NOT EXISTS idx_business_hours_business ON business_hours(business_id, day_of_week, is_active);

